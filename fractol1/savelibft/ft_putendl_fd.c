#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}

//指定されたfdに、文字列sを出力し、その後に改行文字 \n を追加して出力する