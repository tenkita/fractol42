#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	while (s[i])
	{
		f(i, s + i);
		i++;
	}
}

//文字列 s の各文字に対して、関数ポインタ f を適用し、インデックスを一緒に渡す関数です。
// f 関数は各文字のアドレス（ポインタ）を操作することができ、文字列全体を操作する